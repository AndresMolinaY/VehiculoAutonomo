void MPU9250_Init(void)
{
    uint8_t data;

    // Wake up MPU by clearing the sleep bit (PWR_MGMT_1 = 0x6B)
    data = 0x00;
    HAL_I2C_Mem_Write(&hi2c1, MPU_ADDR, 0x6B, 1, &data, 1, HAL_MAX_DELAY);

    // Set clock source to PLL with X axis gyroscope reference
    data = 0x01;
    HAL_I2C_Mem_Write(&hi2c1, MPU_ADDR, 0x6B, 1, &data, 1, HAL_MAX_DELAY);

    // Set accelerometer config to ±2g (ACCEL_CONFIG = 0x1C)
    data = 0x00;
    HAL_I2C_Mem_Write(&hi2c1, MPU_ADDR, 0x1C, 1, &data, 1, HAL_MAX_DELAY);

    // Set accelerometer DLPF (ACCEL_CONFIG_2 = 0x1D) to 44.8Hz
    data = 0x03;
    HAL_I2C_Mem_Write(&hi2c1, MPU_ADDR, 0x1D, 1, &data, 1, HAL_MAX_DELAY);

    // Set gyroscope config to ±250dps (GYRO_CONFIG = 0x1B)
    data = 0x00;
    HAL_I2C_Mem_Write(&hi2c1, MPU_ADDR, 0x1B, 1, &data, 1, HAL_MAX_DELAY);

    // Set gyroscope DLPF (CONFIG = 0x1A) to 41Hz
    data = 0x03;
    HAL_I2C_Mem_Write(&hi2c1, MPU_ADDR, 0x1A, 1, &data, 1, HAL_MAX_DELAY);

    // Set sample rate divider (SMPLRT_DIV = 0x19) to 7 → 1kHz/(1+7)=125Hz
    data = 0x07;
    HAL_I2C_Mem_Write(&hi2c1, MPU_ADDR, 0x19, 1, &data, 1, HAL_MAX_DELAY);
}


